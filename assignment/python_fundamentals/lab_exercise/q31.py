# Q31. Write a Python program that manipulates and prints strings using various string methods.

text = "  bhel puri  "

# Uppercase
print(text.upper())

# Lowercase
print(text.lower())

# Remove spaces
print(text.strip())

# Replace word
print(text.replace("bhel", "dahi"))

# Length of string
print(len(text))

# Check start
print(text.strip().startswith("bhel"))
